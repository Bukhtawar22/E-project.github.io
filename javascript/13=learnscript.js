let day =  7; // yeh sy day change hoga 

switch (day) {  
    case 1:
        console.log("Sonday");
        break;
    case 2:
        console.log("Monday");
        break;
    case 3:
        console.log("Tuesday");        
        break;
    case 4:
        console.log("Wednesday");
        break;
    case 5:
        console.log("Thrusday");
        break;
    case 6:
        console.log("Firday");
        break;
    case 7:
        console.log("Saturday");
        break;    

    default:
        console.log("invild day");
        break;
}